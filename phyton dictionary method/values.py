# Dictionary example
my_dict = {'name': 'John', 'age': 25, 'city': 'Adama'}

# Using values() to get all values
values = my_dict.values()

# Printing the values
print(values)  # Output: dict_values(['John', 25, 'Adama'])

# Iterating through the values
for value in my_dict.values():
    print(value)
# Output:
# John
# 25
# Adama
